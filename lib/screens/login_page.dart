  Future<bool> isUsernameTaken(String name) async {
    final QuerySnapshot result = await Firestore.instance
        .collection('users')
        .where('username', isEqualTo: name)
        .limit(1)
        .getDocuments();
    return result.documents.isEmpty;
  }

