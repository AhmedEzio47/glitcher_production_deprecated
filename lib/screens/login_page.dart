  }

  void _toggleLogin() {
    _formKey.currentState.reset();
    setState(() {
      _formMode = FormMode.LOGIN;
      _obscureTextLogin = !_obscureTextLogin;
    });
  }

  void _toggleSignup() {
    _formKey.currentState.reset();
    setState(() {
      _formMode = FormMode.SIGNUP;
      _obscureTextSignup = !_obscureTextSignup;
    });
  }

